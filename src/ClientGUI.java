    public String getUserName() {
        String input;
        do {
            input = (JOptionPane.showInputDialog(null,
                    "Enter your username:",
                    "Chat App", JOptionPane.PLAIN_MESSAGE));
        } while (input != null && input.length() == 0);

        // Cancel option selected
        if (input == null) {
            System.exit(0);
        }
        this.client.setClientName(input);

        return input;
    }

