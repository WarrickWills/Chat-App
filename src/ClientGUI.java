    private void sendAllButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendAllButtonActionPerformed
        try {
            client.send(new BroadcastMessage(client.getClientNameGUI() + " (ALL): " + message.getText() + "\n"));
            message.setText("");
        } catch (IOException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_sendAllButtonActionPerformed

    private void sendPrivateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendPrivateButtonActionPerformed
        try {
            String sendToClient;
            do {
                sendToClient = (JOptionPane.showInputDialog(null,
                        "Who do you want to send this message to? (Enter Username)",
                        "Chat App", JOptionPane.PLAIN_MESSAGE));
            } while (sendToClient != null && sendToClient.length() == 0);

            // Cancel option selected
            if (sendToClient == null) {
                client.send(new CancelMessage(""));
                JOptionPane.getRootFrame().dispose();
            } else {
                client.send(new MessageTo(client.getClientNameGUI() + " (PRIVATE): " + message.getText() + "\n", sendToClient));
                message.setText("");
            }

        } catch (IOException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_sendPrivateButtonActionPerformed
